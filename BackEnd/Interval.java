package BackEnd;

public class Interval {
    private float time;
    private float distance;
    private float rest;

    public float getTime() { return time; }
    public float getDistance() { return distance; }
    public float getRest() { return rest; }

    public void setTime(float _time) { time = _time; }
    public void setDistance(float _distance) { distance = _distance; }
    public void setRest(float _rest) { rest = _rest; }
}
